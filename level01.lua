function level.Load()
print("Level textures loading...")
  -- get tilesheet png
  level.tileSheet = love.graphics.newImage("images/tilesheet.png")
  -- get number of tilesheet columns/rows
  local nCol = level.tileSheet:getWidth()/screen.tileWidth
  local nRow = level.tileSheet:getHeight()/screen.tileHeight

  level.tileTexture[0] = nil

  local row, col
  local id = 1

  for row = 1, nRow do
    for col = 1, nCol do
      level.tileTexture[id] = love.graphics.newQuad(
        (col - 1) * screen.tileWidth, 
        (row - 1) * screen.tileHeight, 
        screen.tileWidth, 
        screen.tileHeight, 
        level.tileSheet:getWidth(), 
        level.tileSheet:getHeight()
        )
      id = id + 1
    end
  end

  print("...textures loaded")

  -- add enemies
  enemies = require("enemies")
  enemies.SpawnTank(450, 350)
  enemies.SpawnTurret(600, 300)
end


function level.Update(dt)
  player.TextureDetect(level.map, level.tileType, dt)
  enemies.Update(dt)

end

---------------------------------------------------------------
-- Draw
---------------------------------------------------------------
function level.Draw()
  -- boucle de dessin de la map en fonction des textures
  local row, col
  for row = 1, screen.mapHeight do
    for col = 1, screen.mapWidth do
      local ground = level.map.ground[row][col]
      local quadGround = level.tileTexture[ground]
      if quadGround ~= nil then
        love.graphics.draw(
          level.tileSheet,
          quadGround, 
          (col - 1) * screen.tileWidth, 
          (row - 1) * screen.tileHeight
          )
      end
    end
  end

  for row = 1, screen.mapHeight do
    for col = 1, screen.mapWidth do
      local grid = level.map.grid[row][col]
      local quadGrid =   level.tileTexture[grid]
      if quadGrid ~= nil then
        love.graphics.draw(
          level.tileSheet,
          quadGrid, 
          (col - 1) * screen.tileWidth, 
          (row - 1) * screen.tileHeight
          )
      end
    end
  end


end

return level